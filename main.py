mood = input("Mood? ")
if mood == "Happy":
    print("It's great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Don't worry, everything will be fine.")
elif mood == "excited":
    print("Good for you!")
elif mood == "Relaxed":
    print("Enjoy ;)")
else:
    print("I don't recognize this mood.")
